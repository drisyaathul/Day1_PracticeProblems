package com.bridgelabz;

public class PrimitiveDataType {

    byte a;
    short b;
    int c;
    long d;
    float e;
    double f;
    char g ;
    boolean h;

    public static void main(String[] args) {

        PrimitiveDataType dataType = new PrimitiveDataType ();
        System.out.println("The Default value of byte is " +dataType.a);
        System.out.println("The Default value of short is " +dataType.b);
        System.out.println("The Default value of int is " +dataType.c);
        System.out.println("The Default value of long is " +dataType.d);
        System.out.println("The Default value of float is " +dataType.e);
        System.out.println("The Default value of double is " +dataType.f);
        System.out.println("The Default value of char is " +dataType.g);
        System.out.println("The Default value of boolean is " +dataType.h);
    }
}
